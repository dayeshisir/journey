create table `spots` (
  `id`         BIGINT  UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '自增id',
  `w_id`       BIGINT           NOT NULL DEFAULT 0  COMMENT '嗡嗡Id',
  `nick_name`  VARCHAR(128)     NOT NULL DEFAULT '' COMMENT '昵称',
  `pic`        VARCHAR(128)     NOT NULL DEFAULT '' COMMENT '图片',
  `desc`       VARCHAR(128)     NOT NULL DEFAULT '' COMMENT '亮点',
  `mddid`      INT(10) UNSIGNED NOT NULL DEFAULT 0  COMMENT '目的地id',
  `mdd_name`   VARCHAR(128)     NOT NULL DEFAULT '' COMMENT '目的地',
  `label`      INT(10)          NOT NULL DEFAULT 0  COMMENT '当前仅国内 1、国外2，后续可能会有山区、海边等',
  `min_num`    TINYINT          NOT NULL DEFAULT 0  COMMENT '最少合适人数',
  `max_num`    TINYINT          NOT NULL DEFAULT 0  COMMENT '最大合适人数',
  `min_days`   INT(10)          NOT NULL DEFAULT 0  COMMENT '最小推荐天数',
  `max_days`   INT(10)          NOT NULL DEFAULT 0  COMMENT '最大推荐天数',
  `time`       VARCHAR(1024)    NOT NULL DEFAULT '' COMMENT '最宜游玩时间',
  `relation`   TINYINT          NOT NULL DEFAULT 0  COMMENT '关系类型',
  `budget`     TINYINT          NOT NULL DEFAULT 0  COMMENT '预算类型',
  `min_budget` INT(10)          NOT NULL DEFAULT 0  COMMENT '预算范围',
  `max_budget` INT(10)          NOT NULL DEFAULT 0  COMMENT '预算范围',
  `min_sbudget`INT(10)          NOT NULL DEFAULT 0  COMMENT '显示的最小预算',
  `max_sbudget`INT(10)          NOT NULL DEFAULT 0  COMMENT '显示的最大预算',
  `priority`   TINYINT          NOT NULL DEFAULT 0  COMMENT '权重',
  `spread`     VARCHAR(32)      NOT NULL DEFAULT '' COMMENT '推广',
  `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP  COMMENT '新建时间',
  `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP  COMMENT '更新时间',
  PRIMARY KEY (`id`),
  INDEX `idx_budget` (`min_budget`, `max_budget`),
  INDEX `idx_num`(`min_num`, `max_num`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4;