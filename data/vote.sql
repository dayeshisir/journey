CREATE TABLE `votes`(
  `id` BIGINT(20)  UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '自增id',
  `uid` BITINT(20) UNSIGNED NOT NULL DEFAULT 0 COMMENT '用户id',
  `journey_id` BIGINT(20) UNSIGNED NOT NULL DEFAULT 0 COMMENT '旅行id',
  `spot_id`    BIGINT(20) UNSIGNED NOT NULL DEFAULT 0 COMMENT '景点id',
  `vote`       TINYINT    UNSIGNED NOT NULL DEFAULT 0 COMMENT '0:反对 1:赞成',
  `created_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP  COMMENT '新建时间',
  `updated_at` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP  COMMENT '更新时间',
  PRIMARY KEY (`id`),
  INDEX `idx_id`(`journey_id`, `spot_id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8mb4;